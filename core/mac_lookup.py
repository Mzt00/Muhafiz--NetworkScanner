class MacLookup:

    def lookup(self, mac: str) -> tuple[str, str]:
        """
        Look up a MAC address and return (manufacturer, device_type).
        Returns ("unknown", "unknown") if not found.
        """
        if not mac:
            return "unknown", "unknown"

        # Normalise MAC to lowercase colon-separated
        mac_norm = mac.lower().replace("-", ":").replace(".", ":")

        # Try exact 3-octet prefix match
        prefix = ":".join(mac_norm.split(":")[:3])
        if prefix in OUI_DB:
            return OUI_DB[prefix]

        return "unknown", "unknown"

    def enrich_devices(self, devices: list) -> list:
        """
        Enrich devices with MAC OUI manufacturer and device type.
        Only fills in fields that are still 'unknown'.
        Runs after banner grabbing so specific fingerprints take priority.
        """
        enriched = 0
        for device in devices:
            manufacturer, device_type = self.lookup(device.mac)

            if manufacturer == "unknown":
                continue

            # Update device ports with manufacturer/type if still unknown
            for port in device.ports:
                if port.manufacturer == "unknown":
                    port.manufacturer = manufacturer
                if port.device_type == "unknown":
                    port.device_type = device_type

            # If device has no ports at all, still useful to know manufacturer
            if not device.ports:
                # Store in hostname as fallback hint if empty
                if not device.hostname:
                    device.hostname = f"{manufacturer} device"

            enriched += 1
            logger.debug(
                f"MAC OUI: {device.ip} ({':'.join(device.mac.split(':')[:3])}) "
                f"→ {manufacturer} {device_type}"
            )

        logger.info(f"MAC OUI enriched {enriched}/{len(devices)} device(s)")
        return devices